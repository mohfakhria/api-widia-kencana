CREATE EXTENSION IF NOT EXISTS pgcrypto;

CREATE TABLE IF NOT EXISTS document_element_properties (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    token UUID NOT NULL DEFAULT gen_random_uuid(),
    -- Public identifier for frontend communication

    document_element_id BIGINT NOT NULL REFERENCES document_elements (id) ON DELETE CASCADE,
    document_property_id BIGINT NOT NULL REFERENCES document_properties (id) ON DELETE CASCADE,

    default_value TEXT NOT NULL DEFAULT '',
    -- Example: 16, 24, #FFFFFF, flex-start

    position INTEGER NOT NULL DEFAULT 0,

    status TEXT NOT NULL DEFAULT 'active',
    -- Values: active, inactive

    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    CONSTRAINT document_element_properties_status_chk
        CHECK (status IN ('active', 'inactive'))
);

CREATE UNIQUE INDEX IF NOT EXISTS document_element_properties_element_property_uq_idx
    ON document_element_properties (document_element_id, document_property_id);

CREATE UNIQUE INDEX IF NOT EXISTS document_element_properties_token_uq_idx
    ON document_element_properties (token);

CREATE INDEX IF NOT EXISTS document_element_properties_element_position_idx
    ON document_element_properties (document_element_id, position);

CREATE INDEX IF NOT EXISTS document_element_properties_property_idx
    ON document_element_properties (document_property_id);

CREATE INDEX IF NOT EXISTS document_element_properties_status_idx
    ON document_element_properties (status);

